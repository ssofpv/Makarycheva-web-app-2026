# Задание 1: Вычисление факториала
# Реализованы рекурсивная и итерационная функции вычисления факториала
# В блоке if name == 'main' производится сравнение скорости работы

import time

def fact_rec(n):
    """Рекурсивное вычисление факториала числа n"""
    if n < 0:
        raise ValueError("Факториал отрицательного числа не определен")
    if n == 0:
        return 1
    return n * fact_rec(n - 1)

def fact_it(n):
    """Итерационное вычисление факториала числа n"""
    if n < 0:
        raise ValueError("Факториал отрицательного числа не определен")
    result = 1
    for i in range(1, n + 1):
        result *= i
    return result

if __name__ == '_main_':
    # Сравнение скорости работы рекурсивной и итерационной функций
    n = 500
    start = time.time()
    fact_rec(n)
    rec_time = time.time() - start

    start = time.time()
    fact_it(n)
    it_time = time.time() - start

    print(f"Рекурсия: {rec_time:.6f} сек")
    print(f"Итерация: {it_time:.6f} сек")
    # Итерационный метод быстрее из-за отсутствия накладных расходов на вызовы функций

